package com.example.binbolehxfirebase.models;

public class DistrictLocationsModel {

    private int dailyPercent;
    private String weight;

    private String idbin;
    private String district;

    public int getDailyPercent() {
        return dailyPercent;
    }

    public void setDailyPercent(int dailyPercent) {
        this.dailyPercent = dailyPercent;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getIdbin() {
        return idbin;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getDistrict() {
        return district;
    }

    public void setIdbin(String idbin) {
        this.idbin = idbin;
    }
}
